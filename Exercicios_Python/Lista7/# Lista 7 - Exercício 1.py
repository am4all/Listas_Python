# ENTRADA
temperatura = float(input("Digite a temperatura atual (em °C): "))

# PROCESSAMENTO
# Não há cálculos necessários antes da verificação.

# SAÍDA
if temperatura > 38:
    print("Febre detectada")

if temperatura < 38:
     print("Febre não detectada")