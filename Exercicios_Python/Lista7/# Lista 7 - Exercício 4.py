# Escreva um algoritmo que leia um número inteiro. Se o número for menor que zero, mostre a mensagem "O número digitado é negativo".

# ENTRADA
numero = int(input("Digite um número inteiro: "))

# PROCESSAMENTO
# Não há cálculos necessários antes da verificação.

# SAÍDA
if numero < 0:
    print("O número digitado é negativo")
if numero < 0:
    print("O número digitado não é negativo")