# Lista 5 - Exercício 1
# Elabore um programa para calcular a área de um círculo (pi = 3.1415). Fórmula: Área = pi. r**2
# Para 32 - A área do círculo é: 3216.896
# 1. ENTRADA (Recebe os dados)
raio = float(input("Digite o valor do raio: "))

# 2. PROCESSAMENTO (Faz o cálculo)
pi = 3.1415
area = pi * (raio ** 2)

# 3. SAÍDA (Mostra o resultado)
print("A área do círculo é:", area)