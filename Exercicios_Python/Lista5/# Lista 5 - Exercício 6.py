# Lista 5 - Exercício 6
# Escreva um algoritmo que leia o salário base de um funcionário, calcule e mostre o salário reajustado em mais 15%.
# ENTRADA
salario_base = float(input("Digite o salário base do funcionário: "))

# PROCESSAMENTO
salario_reajustado = salario_base + (salario_base * 0.15) 

# SAÍDA
print("O salário reajustado em 15% é:", salario_reajustado)