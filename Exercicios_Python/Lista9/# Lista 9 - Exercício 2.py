# Lista 9 - Exercício 3
# Faça um algoritmo que solicite a idade de uma pessoa. Se ela tiver 18 anos ou mais, exiba a mensagem "Você é maior de idade". Caso contrário, exiba "Você é menor de idade".

# ENTRADA
idade = int(input("Digite a sua idade: "))

# PROCESSAMENTO
# Não há cálculos necessários antes da verificação.

# SAÍDA
if idade >= 18:
    print("Você é maior de idade")
else:
    print("Você é menor de idade")