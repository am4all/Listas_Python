# Lista 9 - Exercício 6
# Faça um programa que leia um número inteiro. Se o número for maior ou igual a zero, exiba no console "Este é um número positivo". Caso contrário, exiba "Este é um número negativo".

# ENTRADA
numero = int(input("Digite um número inteiro: "))

# PROCESSAMENTO
# Não há cálculos necessários antes da verificação.

# SAÍDA
if numero >= 0:
    print("Este é um número positivo")
else:
    print("Este é um número negativo")